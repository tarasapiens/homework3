public class Movie {

    String tittle;
    double rating;
    String genre;
    String country;
    boolean oscar;

    public Movie(String tittle, double rating, String genre, String country, boolean oscar){
        this.tittle = tittle;
        this.rating = rating;
        this.genre = genre;
        this.country = country;
        this.oscar = oscar;

    }
}
